#include <stdio.h>

int main(){
	int N, i, X, Y;
	scanf("%d" ,&N);
	for(i = 0; i < N; i++){
		scanf("%d %d", &X, &Y);
		if(Y != 0)
			printf("%.1f\n", (float) X/Y);
		else
			printf("divisao impossivel\n");
	}
	return 0;
}